package gdcmetadataextractor;

class Demographic {
    private String updated_datetime;
    private String created_datetime;
    private String gender;
    private String state;
    private String submitter_id;
    private String year_of_birth;
    private String race;
    private String demographic_id;
    private String ethnicity;
    private String year_of_death;

    public String getUpdated_datetime() {
        return updated_datetime;
    }

    public void setUpdated_datetime(String updated_datetime) {
        this.updated_datetime = updated_datetime;
    }

    public String getCreated_datetime() {
        return created_datetime;
    }

    public void setCreated_datetime(String created_datetime) {
        this.created_datetime = created_datetime;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public String getSubmitter_id() {
        return submitter_id;
    }

    public void setSubmitter_id(String submitter_id) {
        this.submitter_id = submitter_id;
    }

    public String getYear_of_birth() {
        return year_of_birth;
    }

    public void setYear_of_birth(String year_of_birth) {
        this.year_of_birth = year_of_birth;
    }

    public String getRace() {
        return race;
    }

    public void setRace(String race) {
        this.race = race;
    }

    public String getDemographic_id() {
        return demographic_id;
    }

    public void setDemographic_id(String demographic_id) {
        this.demographic_id = demographic_id;
    }

    public String getEthnicity() {
        return ethnicity;
    }

    public void setEthnicity(String ethnicity) {
        this.ethnicity = ethnicity;
    }

    public String getYear_of_death() {
        return year_of_death;
    }

    public void setYear_of_death(String year_of_death) {
        this.year_of_death = year_of_death;
    }
    
    
}
