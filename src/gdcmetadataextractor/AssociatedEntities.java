package gdcmetadataextractor;

public class AssociatedEntities {
    private String entity_id;
    private String case_id;
    private String entity_submitter_id;
    private String entity_type;

    public String getEntity_id() {
        return entity_id;
    }

    public void setEntity_id(String entity_id) {
        this.entity_id = entity_id;
    }

    public String getCase_id() {
        return case_id;
    }

    public void setCase_id(String case_id) {
        this.case_id = case_id;
    }

    public String getEntity_submitter_id() {
        return entity_submitter_id;
    }

    public void setEntity_submitter_id(String entity_submitter_id) {
        this.entity_submitter_id = entity_submitter_id;
    }

    public String getEntity_type() {
        return entity_type;
    }

    public void setEntity_type(String entity_type) {
        this.entity_type = entity_type;
    }
    
    
}
