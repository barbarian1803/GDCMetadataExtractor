package gdcmetadataextractor;

public class Exposures {
    private String cigarettes_per_day;
    private double weight;
    private String alcohol_history;
    private String intensity;
    private double bmi;
    private String years_smoked;
    private String height;
    private String created_datetime;
    private String state;
    private String exposure_id;
    private String submitter_id;

    public String getCigarettes_per_day() {
        return cigarettes_per_day;
    }

    public void setCigarettes_per_day(String cigarettes_per_day) {
        this.cigarettes_per_day = cigarettes_per_day;
    }

    public double getWeight() {
        return weight;
    }

    public void setWeight(double weight) {
        this.weight = weight;
    }

    public String getAlcohol_history() {
        return alcohol_history;
    }

    public void setAlcohol_history(String alcohol_history) {
        this.alcohol_history = alcohol_history;
    }

    public String getIntensity() {
        return intensity;
    }

    public void setIntensity(String intensity) {
        this.intensity = intensity;
    }

    public double getBmi() {
        return bmi;
    }

    public void setBmi(double bmi) {
        this.bmi = bmi;
    }

    public String getYears_smoked() {
        return years_smoked;
    }

    public void setYears_smoked(String years_smoked) {
        this.years_smoked = years_smoked;
    }

    public String getHeight() {
        return height;
    }

    public void setHeight(String height) {
        this.height = height;
    }

    public String getCreated_datetime() {
        return created_datetime;
    }

    public void setCreated_datetime(String created_datetime) {
        this.created_datetime = created_datetime;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public String getExposure_id() {
        return exposure_id;
    }

    public void setExposure_id(String exposure_id) {
        this.exposure_id = exposure_id;
    }

    public String getSubmitter_id() {
        return submitter_id;
    }

    public void setSubmitter_id(String submitter_id) {
        this.submitter_id = submitter_id;
    }
    
}
